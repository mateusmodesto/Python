n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro número: '))
soma = n1 + n2
m = n1 * n2
d = n1 / n2
e = n1 ** n2
dii = n1 // n2
print('A soma vale {}, a multiplicação vale {}, a divisão vale {:.3f}'.format(soma, m, d))
print('A divisão inteira vale {} e a exponenciação vale {}'.format(dii, e))

